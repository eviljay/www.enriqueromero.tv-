      <h3 class="title">Toros para todos</h3>
		<p>De la mano de su director y presentador, Enrique Romero, "Toros para Todos" regresa a la pantalla de Canal Sur TV con el reto de seguir mostrando de forma amena la vida del toro bravo en el campo.</p>
		<p>Visitaremos las mejores ganader&iacute;as, descubriremos otros "toros diferentes", as&iacute; como otras tauromaquias distintas a la nuestra. Revelaremos el lado m&aacute;s humano de las figuras del toreo con nuestro particular cuestionario y no perderemos detalle con las cr&oacute;nicas de los festejos m&aacute;s atractivos y la actualidad que nos depare la temporada.</p>
		<p>Continuaremos entrevistando a los mejores diestros y visitando las ganader&iacute;as m&aacute;s conocidas con im&aacute;genes de las corridas m&aacute;s emblem&aacute;ticas.</p>
		<p>La pasada temporada Toros para Todos reforz&oacute; su intenci&oacute;n de mostrar una imagen alternativa y diferente del mundo del toro. Un concepto atractivo y moderno acorde con el lenguaje audiovisual que impera en la actualidad.</p>
		<p>Toros para Todos fue el programa taurino m&aacute;s visto de la televisi&oacute;n, siendo l&iacute;der los domingos en Canal Sur TV con una cuota de audiencia superior al 30% y m&aacute;s de cuatrocientos mil espectadores. Adem&aacute;s este &eacute;xito de audiencia se ha visto refrendado por dos grandes premios con que ha sido galardonado Toros para Todos, el Premio Nacional Coss&iacute;o y el de Mejor Programa Cultural, que otorga la Asociaci&oacute;n de Telespectadores de Andaluc&iacute;a (ATEA).</p>
		<p>Toros para Todos se ha convertido as&iacute;, en un espacio de referencia, tanto para los m&aacute;s entusiastas aficionados del mundo del toro, como para un p&uacute;blico general que descubre la belleza y los secretos que encierra esta Fiesta.</p>
